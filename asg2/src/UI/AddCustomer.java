    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String customerId=fieldCustomerid.getText();
        String contact=fieldContact.getText();
        String firstName=fieldFirstname.getText();
        String lastName=fieldLastname.getText();
        
        if (customerId.isBlank()||contact.isBlank()||firstName.isBlank()||lastName.isBlank()){
         JOptionPane.showMessageDialog(this, "All fields are mandatory","Error",JOptionPane.ERROR_MESSAGE);  
        }
        else if(!JudCondition.isAlphabet(firstName)||!JudCondition.isAlphabet(lastName)||!JudCondition.isNumeric(contact)||!JudCondition.isNumeric(customerId)){
            JOptionPane.showMessageDialog(this, "The content you entered is invalid","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
        JOptionPane.showMessageDialog(this, "Successfully createdÔºÅ","Create",JOptionPane.INFORMATION_MESSAGE);    
    }//GEN-LAST:event_btnSaveActionPerformed
       Customer c= customerdirectory.addCustomer();
       c.setContact(contact);
       c.setFirstname(firstName);
       c.setId(customerId);
       c.setLastname(lastName);
       
       
        fieldCustomerid.setText("");
        fieldContact.setText("");
        fieldFirstname.setText("");
        fieldLastname.setText("");
       
    
    
      
    }       

